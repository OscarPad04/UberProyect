    public void initList(){
        String tagPersonal = "METHOD initList() ";
        int cont = 0;
        Trabajo work = new Trabajo();


        try (BufferedReader br = new BufferedReader(new FileReader("Works.dat"))) {
            String linea;

            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split("---");
                
                work = new Trabajo(datos[0], datos[1], datos[2], datos[3], datos[4], datos[5], datos[6]);
                
                if(work.getEstado().equalsIgnoreCase("Disponible")) {
                    String cadena = " Cliente: " + work.getNombreCliente() + " || Ubicacion: " + work.getUbicacionActual() + " || Direccion: " + work.getDireccion() + " || Ofrece: $" + work.getDineroOfrecido();
                    
                    cola.agregarTrabajo(work);
                    modelList.add(cont, cadena);
                    cont++;
                }
            }
        } catch (IOException e) {
            System.out.println(tag + tagPersonal + "Ha ocurrido un error " + e);
        }
    }
    
    private void setImagen(JLabel vista, String root) {
        ImageIcon image = new ImageIcon(root);
        Icon icon = new ImageIcon( image.getImage().getScaledInstance(vista.getWidth(), vista.getHeight(), Image.SCALE_DEFAULT ) );
        vista.setIcon(icon);
        this.repaint();
    }
    
